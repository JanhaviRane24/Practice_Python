# Different data types in Python

# Integer
number = 10
print("Integer:", number)
print("Type:", type(number))

# Float
decimal = 10.5
print("\nFloat:", decimal)
print("Type:", type(decimal))

# String
name = "Python"
print("\nString:", name)
print("Type:", type(name))

# Boolean
is_active = True
print("\nBoolean:", is_active)
print("Type:", type(is_active))

# Complex number
complex_number = 3 + 4j
print("\nComplex:", complex_number)
print("Type:", type(complex_number))


