# variables.py

# Integer
age = 21

# Float
height = 5.8

# String
name = "John"

# Boolean
is_student = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

# Multiple variable assignment
x, y, z = 10, 20, 30
print("x =", x)
print("y =", y)
print("z =", z)

# Same value to multiple variables
a = b = c = 100
print("a =", a)
print("b =", b)
print("c =", c)

# Dynamic typing
value = 50
print("Value:", value)

value = "Python"
print("Updated Value:", value)

# Variable swapping
first = 5
second = 10

first, second = second, first

print("First:", first)
print("Second:", second)
